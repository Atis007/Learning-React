// Handling Multiple Input Fields with ref
// The advantage of using this approach is that it typically requires less code the when using state.
// Since we are not updating state on every keystroke, the component does not re-render as frequently,
// which can lead to better performance, especially in forms with many input fields.

// validating with refs is less optimal, because we don't have access to the input value on every keystroke,
// the validation is done after the user submits the form
import { useRef, useState } from "react";

export default function Login() {
  const [emailIsInvalid, setEmailIsInvalid] = useState(false);

  const email = useRef();
  const password = useRef();

  function handleSubmit(event) {
    event.preventDefault(); // Prevents the default browser behavior (to generate and send an http request)

    const enteredEmail = email.current.value;
    const enteredPassword = password.current.value;

    const emailIsValid = enteredEmail.includes("@");

    if (!emailIsValid) {
      setEmailIsInvalid(true);
      return;
    }

    setEmailIsInvalid(false);
  }

  function handleInputChange(identifier, value) {
    // existing key values are preserved using the spread operator (...prevValues), and only the specific key being updated is changed ([identifier]: event.target.value)
    setEnteredValues((prevValues) => ({
      ...prevValues,
      [identifier]: value,
    }));
  }

  return (
    <form onSubmit={handleSubmit}>
      <h2>Login</h2>

      <div className="control-row">
        <div className="control no-margin">
          <label htmlFor="email">Email</label>
          <input id="email" type="email" name="email" ref={email} />
          <div className="control-error">
            {emailIsInvalid && <p>Please enter a valid email address.</p>}
          </div>
        </div>

        <div className="control no-margin">
          <label htmlFor="password">Password</label>
          <input id="password" type="password" name="password" ref={password} />
        </div>
      </div>

      <p className="form-actions">
        <button className="button button-flat">Reset</button>
        <button className="button">Login</button>
      </p>
    </form>
  );
}
